# =============================================================================
# Figure Generation: Individual Trajectories, 6-Step Predictions, Survival Probabilities
# =============================================================================
# Produces the main manuscript figure for individual-level prediction:
#   - Figure 3: Back-transformed individual biomarker trajectories and six-step-ahead
#     predictions in original clinical units, with Monte Carlo prediction intervals
#     for mortality (representative patient observed to month 35)
#   - An intermediate standardized-scale plot (diagnostic, not in manuscript)
#
#   Note: Figure 2 (population trends with 95% CI) is generated by albumin_fit.R.
#         Figure 1 (10 random patients + KM curve) is a raw-data plot, generated separately.
#
# Run order: 3 of 4
#   1. albumin_fit.R
#   2. bootstrap_ci.R
#   3. albumin_plot.R          <- YOU ARE HERE
#
# Inputs:
#   albumin_data_0509.RData              # data_split_in, z_in, X00_cube_in
#   filter_estimate_OU_withP.R           # Kalman filter function
#   theta_fit_theta_result_3.RData       # theta_result: bootstrap parameter list;
#                                        #   theta_result[[2]] = point estimate (natural scale)
#   theta_fit_fit_joint_albumin_8.RData  # fit_joint_albumin: optim object;
#                                        #   fit_joint_albumin$par = theta_trans_est (log scale)
#   restandardize_for_plot_0505.RData    # mean_alb, sd_alb, mean_idwg, sd_idwg, etc.
#                                        # for back-transforming to original clinical units
#
# Outputs:
#   0509_6pt_3156.png           # intermediate: individual trajectories on standardized scale
#                               # (not used in manuscript; diagnostic check)
#   0509_6pt_130_noCI_0904.pdf  # Figure 3 in manuscript: back-transformed individual
#                               # trajectories (subject i=130, observed to month 35) +
#                               # six-month-ahead mortality probability subplot
#
# Note: Figure 2 (population trends with 95% CI) is generated by albumin_fit.R, not here.
#       Figure 1 (10 random patients + KM curve) is a raw-data plot, generated separately.
#
# Monte Carlo survival probability (Option B, Section 4.3 of paper):
#   M = 120 samples drawn from the joint posterior of the state vector alpha = (alpha_u, alpha_v)
#   at each time point. Samples grouped into G = 20 groups of 6 and averaged (group_size = 6).
#   R = 20,000 random tuples of group indices combined across 6 steps to estimate
#   P(death within 6 months | data up to month t). Shaded region = 95% empirical CI.
# =============================================================================

