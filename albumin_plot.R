safe_chol <- function(S) {
  out <- try(chol(S), silent = TRUE)
  if (inherits(out, "try-error")) chol(S + diag(1e-8, nrow(S))) else out
}

build_store <- function(step, last,
                        a_u_mat, a_v_cube, P0_est, P1_est, P2_est, P3_est,
                        a_u_mat_extra, a_v_cube_extra, P0_est_extra, P1_est_extra, P2_est_extra, P3_est_extra,
                        subj_index) {
  a_store <- matrix(0, 2*q + q, last)
  P_store <- array(0, c(2*q + q, 2*q + q, last))
  if (step <= last - 1) {
    for (j in (1 + step):last) {
      a_store[, j - step] <- c(a_u_mat[, j], a_v_cube[, subj_index, j])
      P_store[, , j - step] <- rbind(
        cbind(P0_est[, , j], P1_est[, , j]),
        cbind(t(P1_est[, , j]), P2_est[, , j] + P3_est[, , j])
      )
    }
  }
  if (step > 0) {
    for (j in step:1) {
      idx_dst <- last - j + 1
      idx_src <- step - j + 1
      a_store[, idx_dst] <- c(a_u_mat_extra[, idx_src], a_v_cube_extra[, subj_index, idx_src])
      P_store[, , idx_dst] <- rbind(
        cbind(P0_est_extra[, , idx_src], P1_est_extra[, , idx_src]),
        cbind(t(P1_est_extra[, , idx_src]), P2_est_extra[, , idx_src] + P3_est_extra[, , idx_src])
      )
    }
  }
  list(a_store = a_store, P_store = P_store)
}

