mean_sd_fmt <- function(x, digits = c(1,1), na.rm = TRUE) {
  m <- mean(x, na.rm = na.rm)
  s <- sd(x, na.rm = na.rm)
  sprintf(paste0("%.", digits[1], "f (%.", digits[2], "f)"), m, s)
}
range_fmt <- function(x, digits = 0, na.rm = TRUE) {
  r <- range(x, na.rm = na.rm)
  fmt <- paste0("%.", digits, "f")
  paste0(sprintf(fmt, r[1]), " – ", sprintf(fmt, r[2]))
}
npct_fmt <- function(vec, level, digits = 0) {
  n <- sum(vec == level, na.rm = TRUE)
  d <- sum(!is.na(vec))
  p <- 100 * n / d
  sprintf(paste0("%d (%.", digits, "f%%)"), n, p)
}

