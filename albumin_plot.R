compute_p_step_independent <- function(a_store, P_store, step_index, last, subj_index) {
  chol_list <- lapply(seq_len(last), function(j) safe_chol(P_store[, , j]))
  p_s <- matrix(NA_real_, nrow = M, ncol = last)
  for (k in 1:M) {
    for (j in 1:last) {
      z_kj <- rnorm(n = nrow(a_store))
      alpha_jk <- a_store[, j] + chol_list[[j]] %*% z_kj
      alpha_u <- alpha_jk[1:(2*q)]
      alpha_v <- alpha_jk[(2*q + 1):(2*q + q)]
